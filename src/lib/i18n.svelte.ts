/** Çeviri. app.language okur → Svelte markup içinde otomatik reaktif. EN'e, sonra anahtara düşer. */
export function t(key: string): string {
  const loc = app.language as Lang;
  return dicts[loc]?.[key] ?? en[key] ?? key;
}
